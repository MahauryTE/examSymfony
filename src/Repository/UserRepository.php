class UserRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry, private UserPasswordHasherInterface $userPasswordHasher)
    {
        parent::__construct($registry, User::class);
    }

    /**
     * Used to upgrade (rehash) the user's password automatically over time.
     */
    public function upgradePassword(UserInterface $user): void
    {

        $user->setPassword( $this->userPasswordHasher->hashPassword($user, $user->getPassword()));
        $this->_em->persist($user);
        $this->_em->flush();
    }


    public function hashPassword(UserInterface $user): User
    {

        $user->setPassword( $this->userPasswordHasher->hashPassword($user, $user->getPassword()));
        return $user;
    }

